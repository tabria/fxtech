              The patterns you see on your chart are the trails left by bulls and bears. Charts patterns can 
help you decide when a <a href="trend_range.php">trend</a> is likely to continue or to <a href="reversal_patterns.php">reverse</a>. 
There are two main groups of patters: continuation and reversal. 
<b>Continuation patterns</b> include <strong>Triangles</strong>, <a href="flag_pennant_rectangle.php#rectangle">Rectangles</a>, <a href="flag_pennant_rectangle.php#flag">Flags</a> and <a href="flag_pennant_rectangle.php">Pennants</a>. 
They suggest <strong>trading</strong> in the direction of the current
 trend.<b>Reversal patterns </b> include <a href="reversal_patterns.php#doubletop">Double Tops</a> and <a href="reversal_patterns.php#doubletop">Double Bottoms</a>, <a href="reversal_patterns.php">Head and Shoulders</a>, <a href="reversal_patterns.php">Inverted Head and Shoulders</a>. When several chart patterns
  point in the same direction, their signals are reinforced. For example, if an 
uptrendline gets broken when a Head and Shoulders top is being completed, they both confirm that the uptrend is ending. When different 
patterns gives conflicting messages, their signals cancel one another, and  it is better not to trade.</div>

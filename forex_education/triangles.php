<h2>Triangles</h2> 
<div class="textarea">A <strong>triangle</strong> is a congestion area whose upper and lower boundaries converge on the right.Triangles can be divided 
 into three major groups, depending on their angles.</div>
<h2><a name="symm">Symmetrical Triangle</a></h2>
<div class="textarea">The upper and lower lines of symmetrical triangles converge at the same angles. If the upper line 
is inclined 30 degrees to the horizontal, then the lower line is also inclined 30 degrees. Symetrical triangles reflect a fair balance 
of power between bulls and bears and more likely to serve as a continuation patterns.</div>


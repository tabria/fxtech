<h2><a name="asc">Ascending Triangle</a></h2>
<div class="textarea">An ascending triangle has a relatively flat upper boundary and rising lower boundery. Its flat upper boundary shows 
that bulls are maintaining their strength and can lift prices to the same level, while bears are losing their ability to drive prices 
lower.</div><center><img  src="../images/content_education/ascending.gif" alt="Ascending Triangle" /></center>
<br/><br/>
<h2><a name="desc">Descending Triangle </a></h2>
<div class="textarea">A descending triangle has a relatively flat lower boundary, while its upper boundary slants down. Its flat lower 
boundary shows that bears are maintaining their strenght and continue to drive prices down, while bulls are losing their capacity to 
lift prices.</div>
<img class="imgcont" src="../images/content_education/desending_triagalnik.gif" alt="Descending Triangle" />
<div class="textarea">Volume trends shrink as <strong>triangles</strong> get older.If volume jumps on a rally toward the upper boundary, an upside <a href="support_resistance.php#breakout">breakout</a> is more likely. If 
volume becomes heavier when prices fall toward the lower boundary, a downside breakout is more likely. Valid breakouts are accompanied 
by a burst of volume - at least 50 percent above the average for the past 5 days. Valid breakouts occur during the first two thirds of a 
<strong>triangle</strong>. It is better not to trade breakout from the last third of a triangle. If prices stagnate all the way into the apex, they are 
likely to remain flat. <strong>Triangles</strong> provide a minimum target for a move following a breakout. Measure the height of a triangle at its base 
and project vertically from th epoint where the triangles was broken. If you are dealing with a small triangle in the midst of a dynamic 
<strong>trend</strong>, that minimum measurement is likely to be exceeded.

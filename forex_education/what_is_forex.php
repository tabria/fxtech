     <div class="nohtitle">What is forex ? &#150; The answer </div>
                
     <div class="textarea">
             <strong>FOREX</strong>  comes from the words <strong>FOR</strong>eign <strong>EX</strong>change. The term foreign exchange market refers to the largest financial market in the world, where world currencies are bought and sold against each other. The forex market itself consists of several exchanges, located in major financial centers worldwide, which has made the buying and selling of currencies. All <a href="market_participants.php">market participants</a> are connected via phones or computers and they can trade on all exchanges.
<br/><br/>The main idea of the forex market is that to buy a <a href="currency_transactions.php">currency</a>, you have to sell another currency. Yeah, I know, seem quite confusing, but it is a fallacy. Here for example you have US Dollars and you want to buy some Euro. So you are selling your dollars to someone who owns euros and simultaneously  you are buying euros from him.
<br/><br/>The base of the forex market is the <strong>exchange rate</strong>. This is the rate at which its own local currency can be exchanged for units of a foreign currency. This 
system of global trading in foreign currency is known as the <strong>Foreign Exchange Market</strong>, or <strong>Forex</strong> or <strong>Spot</strong>
     </div>
     

    <h2><a name="24hmarket">Here is the mechanism of the 24h market</a></h2>
    <div class="textarea">
First, the market opens  in Sydney  on Sunday at 5:00pm EST (10:00pm GMT), followed by the exchange in Tokyo. At 2:00am EST (7:00am GMT) on Monday the exchange in Sydney closes but Tokyo exchange remains open. After 1h at 3:00am EST (8:00am GMT) the stock market opens in London, and at 4:00am EST (9:00am GMT) Tokyo Stock Exchange is closed, but London remains open. At 8:00am EST (1:00pm GMT) the stock market opens in in New York and at 12:00am EST (5:00pm GMT) London Stock Exchange is closed, but New York remains open. When it closed, the stock market opens in Sydney and it all starts again. Trading ends Friday at 5pm EST (10:00 pm GMT), when the stock market closes in New York, thus ending the 5 day, 24h session of the forex market.

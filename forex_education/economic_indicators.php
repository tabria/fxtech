<h2><a name="gdp">Gross Domestic Product &ndash; GDP </a></h2>
<div class="textarea">Measures the market value of goods and services produced in the economy for a certain period of time, 
usually a year or a quarter. It does not include expenditures on durable assets. It includes only end 
consumption goods or investments.</div>
<h2><a name="hicp">Harmonized Index of Consumer Prices -  HICP</a></h2>
<div class="textarea">HICP is the official inflation measure for the Euro zone. The shift in price stability interpretation 
from below 2% to close to 2% gives the ECB added flexibility in tacking downside price risks.</div>
<h2><a name="housing_starts">Housing Starts and Building permits</a></h2>
 <div class="textarea">Starts are divided into single-family and multi-family categories. In both cases, a housing unit is 
considered &quot;started&quot; when excavation actually begins. Changes in interest rates, especially mortgage 
rates usually impact housing. Building permits are issued for all constructions (except the confidential)
, including temporary constructions, summer kitchens, single garages, etc.</div>
<h2><a name="leading_index">Index of Leading Economic Indicators</a></h2>
<div class="textarea">The LEI is a composite of 10 different indicators, designed to predict future aggregate economic 
activity. The LEI&rsquo;s individual components are selected from various sectors of the economy, 
including manufacturing, building, financial, retail and consumer variables. The components were chosen 
because of their economic relevance and statistical adequacy.</div>
<h2><a name="capacity">Industrial Production and Capacity Utilization</a></h2>
<div class="textarea">Industrial production measures the monthly percentage change in volume of output of the nation&rsquo;s 
factories, mines, and utilities. Capacity utilization measures the extent to which the capital stock is 
employed in production.</div>
<h2><a name="new_home_sales">New Home Sales</a></h2>
<div class="textarea">This index shows the number of new-built homes sold during a certain month. The report also contains 
information on home prices, and number of houses for sale. Housing is a crucial segment of the economy 
because it signals changes in consumer spending patterns that are indicative of economic activity.</div>
<h2><a name="personal_income">Personal Income and Personal Consumption Expenditures</a></h2>
<div class="textarea">Personal income represents the change in compensation that individuals receive from all sources 
including: wages and salaries; proprietors&rsquo; income; income from rents; dividends and interest; and 
transfer payments, etc. Personal consumption expenditures index measures the purchase of durable and 
nondurable goods, as well as services, by consumers.</div>
<h2><a name="ppi">Producer Price Index</a></h2>
<div class="textarea">Measures the average price level of a certain basket of consumer and capital goods purchased by 
producers.</div>
<h2><a name="productivity">Productivity</a></h2>
<div class="textarea">Quarterly measure of the change in the amount of goods and services produced per unit of input. The 
importance of productivity has grown over the past few years since the Federal Reserve has begun 
attributing its growth trend to relatively low levels of inflation.</div>
<h2><a name="retail_sales">Retail Sales</a></h2>
<div class="textarea">Measures the percentage monthly change in total receipts of retail stores, and includes both durable and 
non-durable goods. It is the first real indication of the strength of consumer expenditure.</div>
<h2><a name="napm">National Association of Purchasing managers - NAPM</a></h2>
<div class="textarea">Highly valued for its timeliness and breadth of information, the headline figure is a function of six 
major components: prices paid; new orders; supplier deliveries; production, inventories and employment. 
Note that the latter three components reflect supply forces, while the former three cover demand forces.

<h2><a name="constr_spend">Construction Spending</a></h2>
<div class="textarea">Construction spending measures the value of construction during the course of a 
particular month.</div>
<h2><a name="cpi">Consumer Price Index</a></h2>
<div class="textarea">Measures the change in prices at the consumer level for a fixed basket of goods and services paid for by 
a typical consumer. Items included in the CPI reflect prices of food, clothing, shelter, fuels, 
transportation, health care and all other goods and services that people buy for day-to-day living. These 
items are divided into seven categories: housing, food, transportation, medical care, apparel, 
entertainment, and other; each of which is weighted by their relative importance. Since food and energy 
prices could fluctuate due to conditions that are unrelated to the economy &ndash; such as weather, oil supply 
or wars &ndash; it is important to break down the factors impacting the change in prices.</div>
<h2><a name="curr_acc">Current Account</a></h2>
<div class="textarea">The most important part of international trade data. It is the broadest measure of sales and purchases of 
goods, services, interest payments and unilateral transfers.
<b>Durable Goods Orders</b> - These include large ticket items such as capital goods (machinery, plant and equipment), transportation 
and defense orders. They are extremely important in that they anticipate changes in production and thus, 
signal turns in the economic cycle.</div>
<h2><a name="eci">Employment Cost Index - ECI</a></h2>
<div class="textarea">The ECI measures changes in employment costs of money wages and salaries and non-cash benefits in 
non-farm industries. The ECI does not include federal government workers.</div>
<h2><a name="empl_rep">Employment Report</a> </h2>
<div class="textarea">In the US, the employment report, also known as the labor report, is regarded as the most important among 
all economic indicators. The unemployment rate is one of the main components of the report and it 
measures the percentage of the civilian labor force actively looking for employment but unable to 
find jobs.</div>
<h2><a name="jobless">Jobless Claims</a></h2>
<div class="textarea">An important indicator of the US labor market condition. The Labor Department announces the number of 
jobless claims and its average during the last four weeks. This data gives information about the total 
level of unemployment in the country.</div>
<h2><a name="factory_orders">Factory Orders and Manufacturing Inventories</a></h2>
<div class="textarea">The factory orders report contains data on orders and shipments of durable and nondurable goods, 
manufacturing inventories, etc. Order data are useful because they tell us something about the likely 
pace of production in the months ahead.</div>

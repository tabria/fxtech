The <strong>economic indicators</strong> included in this lesson are of a practical importance 
for trading on <strong>foreign exchange markets</strong>. While these indicators are generally applicable economic terms,
some of them are specific for the country of their release.</div>
<h2><a name="auto_sales">Auto Sales</a></h2>
 <div class="textarea">The number of cars sold during a particular ten-day period. The timeliness of this indicator makes this 
the most current piece of US economic data.</div>
<h2><a name="balance_payment">Balance of Payments</a></h2>
<div class="textarea">Complete summary of a nation&rsquo;s economic transactions and the rest of the world 
including merchandise, services, financial assets and tourism. The balance of payments is separated into 
two main accounts: the current account and the capital account.</div>
<h2><a name="balance_trade">Balance of Trade</a></h2>
<div class="textarea">The difference between a nation&rsquo;s exports and imports of merchandise. A positive balance of trade, or a 
surplus, occurs when a country&rsquo;s exports exceed its imports. A negative balance of trade, or a deficit, 
occurs when imports surpass exports.</div> 

<h2><a name="beige_book">Beige Book</a></h2>
<div class="textarea">Information on current economic and business conditions in the districts defined by the Federal Reserve 
Bank, and interviews with key business contacts, economists, market experts, and other sources. The Beige 
Book usually serves as a helpful indicator to Federal Open Market Committee, and it is released prior to 
each FOMC meeting concerning the determination of the interest rates in USA.</div>
<h2><a name="bis">Business Inventories and Sales</a></h2>
<div class="textarea">Data from other reports such as durable goods orders, factory orders, retail sales, and wholesale 
inventories and sales data. Inventories are an important component of the GDP report because they help 
distinguish which part of total output produced remained unsold.</div>
 

<h2><a name="capital_acc">Capital Account</a></h2>
 <div class="textarea">Records a nation&rsquo;s incoming and outgoing investment flows such as payments for entire or parts of 
companies (direct or portfolio investment), stocks, bonds, bank accounts, real estate and factories. 
The balance of payments is influenced by many factors, including the financial and economic climate of 
other countries.</div>


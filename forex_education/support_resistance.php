<h2>Support</h2>
<div class="textarea"><strong>Support</strong> is a price level where buying is strong enough to interrupt or reverse a downtrend. When a downtrend hits support, 
it bounces. Support represented on a chart by a horizontal or near-horizontal line connecting several bottoms.</div>
<div class="imgspace1"><img class="imgcont" src="../images/content_education/support.png" alt="Support" title="Support" /></div>
<br clear="all" />
<h2><a name="resistance">Resistance</a></h2>
<div class="textarea"><strong>Resistance</strong> is a price level where selling is strong enough to interrupt or reverse an uptrend. When an uptrend hits 
resistance, it stops or tumbles down. Resistance represented on a chart by a horizontal or near-horizontal line connecting several tops.</div>
<div class="imgspace"><img class="imgcont" src="../images/content_education/resistance.png" alt="Resistance" title="Resistance" /></div>
<div class="textarea">Minor support or resistance causes trends to pause, while major support or resistance causes them to reverse. <strong>Traders</strong> buy at support and sell
 at resistance.</div>
<div class="textarea">Support and resistance exist because masses of traders feel pain and regret. Traders who  hold losing positions feel intense pain.
Losers are determined to get out as soon as the market gives them another chance.Traders who missed an opportunity feel regret and
also wait for the market to give them a second chance. When the market stays flat for a while, traders get used to buying at the lower
edge of the range and shorting at the upper edge. In uptrends, bears who sold short feel pain and bulls feel regret that did not buy 
more. Both feel determined to buy if the market gives them a second chance. The pain of bears and regret of bulls make them ready to 
buy, creating support during reactions in an uptrend. Resistance is an area where bulls feel pain, bears feel regret, and both are ready 
to sell. The strenght of support and resistance depends on the strength of feeling among masses of traders.</div>
<h2><a name="strongresistance">Strength of Support and Resistance</a></h2>
 <div class="textarea">A congection area that has been hit by several <strong>trends</strong> is like a cratered battlefield. Its defenders have plenty of cover, and an 
attacking force is likely to slow down.

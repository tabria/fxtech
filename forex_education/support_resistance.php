 The longer price stay in a congestion zone, the stronger the emotional commitment of bulls and 
bears to that area.When prices approach that zone from above, it serves as <strong>support</strong>. When price rally into it from below, it acts as 
<strong>resistance</strong>. A congestion area can reverse its role and serve as either suport or resistance.</div>
<div class="imgspace"><img class="imgcont" src="../images/content_education/support_into_resistance.png" alt="Resistance into Support" title="Resistance into Support" /></div>
<div class="textarea">The strength of every support and 
resistance zone depends on three factors: its length, its height, and the volume of trading that has taken place in it. In short this is 
the length, the width, and the depth of a congestion zone. The longer a support or resistance area- its length of time or the number of 
hits it took - the stronger it is. The strength of support and resistance increases each time that area is hit. When <strong>trades</strong> see that 
price have reversed at certain levels, they tend to bet on a reversal the next time prices reach that level. The taller the support and 
resistance zone, the stronger it is. The grater the volume of trading in a support and resistance zone, the stronger it is. High volume 

in congestion area shows active involvement by traders - a sign of strong emotional commitment. Low volume shows that traders have 
little interest in transacting at the level - a sign of week support or resistance.</div> 
<h2><a name="breakout">True Breakout and False Breakout</a></h2>
<div class="textarea">Market spend more time in <a href="trend_range.php">trading ranges</a> than they do in trends. Most <strong>breakouts</strong> from trading ranges are false breakouts. They suck in 
trend-fol-lowers just before prices return into the trading range. True breakouts are confirmed by heavy volume, whike false breakouts 
tend to have light volume.</div>
<div class="imgspace"><img class="imgcont1" src="../images/content_education/false_breakout1.gif"alt="false breakout"/></div>

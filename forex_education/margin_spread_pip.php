<h2><a name="spread">Spread</a></h2>
<div class="textarea">When you trade with currency Your broker will always give you two prices - 
the one is that you can buy on it and the other is the one you can sell on the definite currency. The 
difference between the rate of buy and sell is called <strong>spread</strong> ( the English spread - the difference between 
two prices ). One <strong>pip</strong> is equal to 0.0001 of the currency rate ( exept the quoting of JPY where the 
difference is 0.01 ).</div>
<br/>
<img class="imgmarg" src="../images/content_education/pipettes.gif" alt="Pipette" border="0px"/>
<h2><a name="pip">Pip</a></h2>
<div class="textarea">In a <strong>forex</strong> <a href="../forex_education/currency_transactions.php#quote">quote</a>, for example, EUR/USD, the first currency quoted (EUR) is known as 
 the <a href="../forex_education/currency_transactions.php#base">base currency</a>. The second currency (USD) is the quoted currency. In a forex spot quote, the bid and ask 
 refer to the base currency.The market maker will only quote the last two digits of the bid 
 and offer rate. For example, a EUR/USD quote of 0.8842/47 is quoted orally by the market maker as &#147;forty 
 two, seven.&#148; These are known as the <strong>&#145;pips&#146; or &#145;points&#146;</strong>. The first part of the quote, in this case, 
 0.88 is not quoted. This is known as the &#147;big figure&#148;.</div>

 <h2><a name="pipettes">Pipettes</a></h2>
<div class="textarea"><strong>Pipettes</strong> are fractional pips. 1 pippette = 0.1 pips or 10 pipettes = 1 pips. With pipettes the quote will be with 5 digits - EUR/USD 0.88421/0.88431 instead with 4.

               <h2>Margin</h2>
<div class="textarea">
 <!--ads 300x250-->
<!-- square ad in content end  -->
Trading currencies on <strong>margin</strong> lets you increase your buying power. Here's a 
 simplified example: If you have $2,000 cash in a <strong>margin account</strong> that allows 100:1 <a href="../forex_education/market_participants.php#leverageaccount">leverage</a>, you could 
 purchase up to $200,000 worth of currency-because you only have to post 1% of the purchase price as 
 collateral. Another way of saying this is that you have $200,000 in buying power.With more buying power, you can increase your total
  return on investment with less cash outlay. To be sure, <strong>trading on margin</strong> magnifies your profits AND your losses.
  <h3>For Example :</h3>With a US$5,000 balance in your <strong>margin account</strong>, you decide that the  Euro (EUR)
 will rise against the US Dollar (USD).To execute this strategy, you must buy Euros (simultaneously sell Dollars), and then wait for 
 the exchange rate to rise. The current bid/ask price for EUR/USD is 1.3000/1.3004 (meaning you can buy 1&euro;  for 1.3004 USD 
 or sell $1 US for 1.3000 EUR). Your available <strong>leverage</strong> is 100:1 or 1%. You execute the trade,  buying 100,000 EUR (1 Lot)
 and selling 130,040 USD. At 100:1 leverage, your initial margin deposit for this trade is $1,000. Your account balance is now 
$4000. As you expected, EUR/USD rises to 1.3020/24. You can now sell 1&euro; for 1.3020 USD or buy 1 &euro; for 
1.3024 USD. Since you're long euros , you must now sell euros and buy back 
the dollars to realize any profit. You close out the position, selling one lot (selling 100,000 EUR  and receiving 130,200 USD) Since 
you originally sold (paid) 130,040 USD, your profit is 160 USD.</div>

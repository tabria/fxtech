When you trade with <a href="what_is_forex.php">forex</a><strong> currency</strong> you always choose a combination of two <strong>forex currency </strong>- 
for example you would buy American dollar ( USD ) and simultaneously sell Japanese yen ( JPY ) or you buy euro ( EUR ) and sell

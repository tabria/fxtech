<h2><a name="spottransactions">Spot transactions</a></h2>
<div class="textarea">The relative values of the world&#146;s currencies affect virtually every 
financial and economic trend. Business can be battered by foreign competition when the currency soars. 
Inflation often soars when the domestic currency plunges.According to a survey by the Bank for International Settlements (BIS), 
every day the equivalent of over USD 1 trillion is converted from one currency to another on the spot foreign exchange market.A <strong>spot forex 
transaction</strong> is an exchange of one currency against another at an agreed rate, settlement of which takes place two working days later.
</div>
<h3>For example :</h3>
 <div class="example">A spot trade done on Monday will settle on Wednesday, and a spot trade done on Friday will 
settle on Tuesday. The settlement date is known as the value date.The Canadian dollar (CAD) is the exception. When traded against the 
US dollar (USD), settlement takes place one working day after the trade date. One day spot USD/CAD is known in the market as funds.</div>

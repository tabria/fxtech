<h2><a name="quote">Components of a Spot Quote</a></h2>
<div class="textarea">A <strong>spot rate</strong> is the price of one currency against another expressed as a ratio. 
It consists of two parts: one is the market maker&#146;s buying rate and the other is the selling rate. The buying rate is the <b>bid</b>. 
The selling rate is the <b>ask (offer).</b></div>
<h3>Example :</h3>
 <div class="example">A EUR/USD <strong>bid</strong> rate is the rate at which the market maker buys euro against the US dollar. 
Given a bid rate of 0.8842, for each euro received, 0.8842 US dollars must be paid. The last two figures of the bid rate 
are substituted with the figures pertaining to the offer rate. For example, an offer rate of 47 is an 
abbreviation of 0.8847. The full <strong>quote</strong> is 0.8842/47.</div>
<div class="textarea">
The market maker would <strong>quote</strong> both the bid and offer prices in the market, that is, a two-way price. 
The difference between the buying and selling rate is known as the <a href="margin_spread_pip.php#spread">spread</a>.</div>
<h2><a name="basecurrency">Base Currency vs Quoted Currency</a></h2>
 <div class="textarea">In a forex quote, for example, EUR/USD, the first currency quoted (EUR) is 
known as the <strong>base currency</strong>. The second currency (USD) is the quoted currency. In a spot quote, the bid and offer refer to the base 
currency.The market maker will only quote the last two digits of the bid and offer rate.</div> 
<h3>Example :</h3>
 <div class="example"> EUR/USD 
quote of 0.88<b>42/47</b> is quoted orally by the market maker as <b>&#147;forty two, seven.&#148;</b> These are known as the 
<a href="margin_spread_pip.php#pip">&#145;pips&#146; or &#145;points&#146;</a>. The first part of the quote, in this case, 0.88 is not quoted. This is known as the 
&#147;big figure&#148;.</div>
<h2><a name="cross">Cross Rates</a></h2>
<div class="textarea">Forex rates typically involve currencies quoted against the US dollar, for example, EUR/USD, 
GBP/USD and USD/JPY. However there are other quotes known as <strong>cross rates</strong>, where neither currency is the US dollar.</div>
 <h3> For Example :</h3>
<div class="example">EUR/JPY ; EUR/GBP ; EUR/CHF ; GBP/JPY ; CHF/JPY ; GBP/CHF. 
<br/>In the EUR/JPY <strong>cross rate</strong>, the <strong>base currency</strong> is EUR (the first 
currency in the currency pair). Therefore, a market maker will buy EUR (base currency) against JPY at the bid rate and sell EUR 
(base currency) against JPY at the offer rate.</div>
<h2><a name="quoting">Methods of Quoting Exchange Rates</a></h2>
<div class="textarea">There are two methods of quoting exchange rates:A direct quotation shows a variable amount of domestic currency against a fixed amount of foreign currency. 
In dealing circles, <strong>direct quotes</strong> are ones where the US dollar is the base currency, for example, USD/JPY 
and USD/CHF. An indirect quotation shows a variable amount of foreign currency against a fixed amount of 
the domestic currency. In dealing circles, <strong>indirect quotes</strong> are ones where the US dollar is not the base 
currency, for example, GBP/USD and EUR/USD.</div>
<h2><a name="forward">Forward</a></h2>
<div class="textarea">Except <strong>spot forex transactions</strong> in the global foreign exchange trading there are also transactions, 
settlement of which takes place at some fixed or open date in the future. These are so-called <strong>Forward</strong> 
transactions. They are used to fix a rate today for a transaction to be settled at some fixed or open 
date in the future. Thus companies can plan the settlements of their trade operations. These transactions 
are used mainly for hedging the risk of abrupt movements in the value of a currency pair. Forward rates 
always move to a level that shows the interest rate divergences.


              <h2>Trend</h2>
<div class="textarea">
 <!-- square ad in content start  -->
<div class="adsincontenttop">
<!--ads 300x250-->
</div><!-- square ad in content end  -->
<strong>Traders</strong> try to profit from changes in prices: Buy low and sell high, or sell short high and 
cover low.Even a quicq look at a chart reveals that markets spend most of their time in <strong>trading ranges</strong>.
</div>
<div class="textarea">
 They spend less time in <strong>trends</strong>. 
A trend exist when prices keep rising or falling over time. In an <b>uptrend</b>, each rally reaches a higher high than the preceding 
rally and each decline stops at a higher level than the preceding decline. In a <b>downtrand</b>, each decline falls to a lower low than preceding decline and each rally stops at 
lower level than the preceding rally.In a <b>trading range</b>, most rallies stop at about the same high and declines peter out at about
 same low.</div>
<div class="imgspace"><img class="imgcont" src="../images/content_education/trend.png" alt="trend" title="Trend and Trading Range" /></div>
<div class="textarea">Trading in <strong>trends</strong> and trading ranges calls for diferent tactics. When you go long in an uptrend or sell short in a downtrend, you have to give that trend time to develop and you have to stay with this trend as long as he continues.When you trade in a trading range, you have to act fast and close out your position at the slightest sign of a <a href="reversal_patterns.php">reversal</a>.
Another difference in <strong>trading</strong> tactics between trends and trading ranges is the handling of strength and weakness. You have to follow 
strength during trends - buy in uptrends and sell short in downtrends. When prices are in a trading range, you have to do the opposite - 
buy weakness and sell strength.</div>
<h2><a name="trendpsy">Trend Psychology</a></h2>
<div class="textarea">An <strong>uptrend</strong> emerges when bulls are stronger than bears and their buying forces prices up. If bears manage to push prices down, bulls 
return in force, break the decline, and force price to a new high. <strong>Downtrend</strong> occur when bears are stronger and their selling pushes 
market down. Wen flurry of buying lifts prices, bears sell short into that rally, stop it, and send prices to new lows. When bulls and 
bears are equally strong or weak, prices stay in a trading range. When bulls manage to push prices up, bears sell short and prices fall.
 Prices in <strong>trading ranges</strong> go nowhere.

broking in recent years, together with the introduction of the single currency (the euro), has greatly 
diminished the role of the voice broker.</div>
 <h2><a name="bank">Institutions</a></h2>
<div class="textarea"><strong>Institutions</strong> such as the <a href="http://www.ecb.europa.eu" target="_blank">European Central Bank (ECB)</a>, the <a href="http://www.federalreserve.gov/" target="_blank">US Federal Reserve 
 Bank (FED)</a>, the <a href="http://www.bankofengland.co.uk/" target="_blank">Bank of England (BOE)</a>, the Swiss National Bank, and the Bank of Japan. Central authorities enter the 
 <strong>foreign exchange market</strong> for several reasons to strengthen or weaken its own currency or to assist another central institution to do the same. 
to switch reserves from one currency to another. to smooth out any undesirable fluctuations in an exchange rate.</div>
  <h2><a name="corporation">Corporation</a></h2>
<div class="textarea"> Corporation - the companies involved in international trade will enter the forex market to manage their 
 cash flows. As a result of their international trade, these companies will be exposed to foreign exchange risk and will need to 
protect themselves against adverse movements in the forex market by hedging their positions. For example, 
Japanese companies are net exporters to the US and receive payment in US dollars. They naturally need to 
sell dollars against the yen and are therefore exposed to any weakening of the US dollar against the yen. 
Companies operating globally may also need to repatriate profits in other currencies to their base 
currency. For example, a company with its Head Office in London and subsidiaries in Europe and America 
may want to bring back its profits in euro and US dollars to its base currency, sterling, for balance 
sheet purposes. They will therefore need to buy sterling and sell euro and US dollars. Some companies 
have their own in-house dealing rooms and consequently will act as quasi-institutions in the market; they will 
take on currency risk and will be involved in trading and speculating.</div>
<h2><a name="fundmanagers">Fund managers</a></h2>
<div class="textarea">Fund managers are institutions or individuals that manage investment portfolios either for their own 
account or on behalf of their customers. When they invest in the international stock and bond markets, 
they will have a natural need to move from one currency to another. Investment flows also arise from fund 
managers switching economies.</div>
<h2><a name="leverageaccount">Leveraged accounts</a></h2>
<div class="textarea">Leveraged accounts are high net worth individuals (private customers), as well as institutions, that are 
involved in what is called <strong><a href="margin_spread_pip.php">margin</a></strong> trading. This allows them to speculate in amounts that are far larger 
than their available capital. They will need to post collateral (usually US Treasuries) with the institution in a 
blocked account. The institution would agree a <strong>leverage</strong> multiple which both sides find acceptable. For example, 
the multiple might be 10 times or even 400 times.  This means that the investor can post collateral of USD 10 million and 
can subsequently trade up to USD 100 million or USD 400 millions in the <strong>forex market</strong>.

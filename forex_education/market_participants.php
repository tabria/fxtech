 <strong>Market Makers</strong> are the foreign exchange dealers in commercial and investment institution dealing 
rooms <strong><a href="currency_transactions.php#quote">quote</a></strong> exchange rates in the inter-institution market, that is, between institutions. They <strong>quote</strong> prices at which they are prepared to buy 
or sell currencies and therefore make a market. The activity of <strong>forex dealers</strong> can be speculative where they try to 
make a profit from anticipating exchange rate movements.</div>
<h3>Market Makers Quote</h3>
 <div class="textarea">Another part of their activity involves <a href="currency_transactions.php#quote"> quoting prices </a> both internally and externally. Internally would involve <strong>quoting</strong> prices to the other dealing desks 
within the dealing room, for example, to the currency options desk, interest rate derivatives desk, forex 
forwards desk, money markets desk and the proprietary trading desk. Externally would involve quoting 
prices to their customer base, for example, international corporations or fund managers. In recent years, 
consolidation of the institutioning industry has led to a contraction of active <strong>market makers</strong>. The BIS 2001 
survey reveals that, in the UK, 17 institutions captured 75% of the market activity in 2001 compared to 24 institutions 
in 1998 and 20 institutions in 1995. In the US, the data indicates that 75% of<strong><a href="currency_transactions.php"> forex transactions </a></strong>was conducted by 
only 13 institutions in 2001 compared to 20 institutions in 1998 and 1995.</div>
<h2><a name="brokers">Brokers</a></h2> 
<div class="textarea"><strong>Brokers</strong> do not quote their own exchange rates; they are not market makers. Rather, they act as 
intermediaries between the <strong>market makers</strong> and relay the best prices, trying to match buy and sell orders. 


<h2><a name="inverted">Inverted Head and Shoulders</a></h2>
<div class="textarea">This is a mirror image of a normal Head and Shoulders - the head at the lowest point, surrounded by two shoulders. This pattern develops 
when a downtrend loses its force and gets ready to reverse.</div>
<br/><center><img src="../images/content_education/head_shoulders_obratna.gif" alt="Inverted Head and Shoulders" /></center>
<div class="textarea">In a valid downtrend, each new low falls lower than the previous low, and cach rally stops at a lower level. A 
strong rally from the head allows you to draw a neckline. When a decline from the neckline fals to reach the level of the head, it 
creates the right shoulder. When prices rally from the right shoulder above the neckline on increased volume, they complete the <strong>Invertd 
Head and Shoulders</strong> and a new uptrend is begins. Sometimes a Head and Shoulders is followed ny a pullback on the neckline on low volume, 
offering an excellent buying opportunity. Measure the distance from the bottom of the head to the neckline and project it upward from 
the point where the neckline was <a href="support_resistance.php#breakout">broken</a>. This gives you a minimum measurement for rally, which is frequently exceeded. </div>
<h2><a name="doubletop">Double Top and Double Bottom</a></h2>
<div class="textarea"><strong>Double Top</strong> occure when prices rally to the area of the previous hight. <strong>Double Bottom</strong> occur when prices fall 
near the previous low. The second top or bottom can be slightly above or below the first. This often confuses beginning analysts.</div>
<div class="imgspace"><img class="imgcont" src="../images/content_education/double_bottom.png" alt="Double Bottom" title="Double Bottom" /></div> <br />
<div class="textarea">Most of the <strong>traders</strong> use technical indicators to identify double tops and bottoms. They are often marked by 
bullish and bearish divergences. Buying at double bottom and selling short at double tops offer some of the best <strong>trading</strong> opportunities.

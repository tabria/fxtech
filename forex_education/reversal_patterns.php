               <h2>Head and Shoulders</h2>
<div class="textarea">
 <!-- square ad in content start  -->
<div class="adsincontenttop">
<!--ads 300x250 -->
</div><!-- square ad in content end  -->
A healthy uptrend moves up in steps. Most rallies higher peaks than the preceding rally and declines stop at a 
higher level than the preious decline. When an uptrend fails to reach a higher high or a decline falls below the previouse low, it shows 
that bulls are losing their grip. <strong>Head and Shoulders</strong> marks the end of uptrends. The <strong>"head"</strong> is a price peak surrounded by two lower peaks, 
or <strong>"shoulders"</strong>. A neckline connects the lows of declines from the left shoulders and the head. The <strong>neckline</strong> does not have to be 
horizontal - it may be flat, rising, or falling. A downsloping neckline is especially bearish - it shows that bears are becoming stronger.</div>
<div class="textarea">When price fail to rally above the head, they confirm that a <strong>Head and Shoulders</strong> is developing. The right shoulder 
may be higher or lower than the left and may be shorter or longer.
<div class="imgspace"><img class="imgcont" src="../images/content_education/head_shoulders.gif" alt="Head and Shoulders" title="Head and Shoulders" /></div> 
The decline from the right shoulder breaks the neckline. When that 
happens, the uptrend is dead. After <a href="support_resistance.php#breakout">breaking</a> the <strong>neckline</strong>, price somtimes pull back to it on low volume. This feeble rally offers an 
excellent opportunity, with a logical stop just above the neckline.</div>
<div class="textarea"><strong>Head and Shoulders</strong> often have typical volume patterns. Volume is often lower on the head than on the left shoulder. 
It is even lower on the right shoulders. Volume tends to increase when prices break the neckline. When prices pull back to it, volume is 
very thin. A Head and Shoulders pattern provides an approximate target for the new downtrend. You can obtain it by measuring the distance 
from the top of head to the neckline and projecting this down from the neckline.</div>

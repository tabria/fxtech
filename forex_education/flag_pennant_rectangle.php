<div class="textarea">If the volume swells when price approach the upper border of a rectangle, an 
<strong>upside breakout</strong> is more likely. If volume increases when prices approach the lower border, a <strong>downside breakout</strong> is more likely. A valid 
breakout from a rectangle is usually confirmed by an increase in volume. If volume is thin, it is likely to be a false breakout.
<div class="imgspace"><img class="imgcont" src="../images/content_education/rectangle.png" alt="Rectangle" title="Rectangle" /></div>
 Rectangles 
trend to be wider in uptrends and narrower in downtrends. The longer <strong>rectangle</strong>, produce  more significant breakout. <strong>Breakout</strong> from rectangles 
on weekly charts are especially important because they mark important victories from bulls or bears. </div>
<div class="textarea">To project how far a <strong>breakout</strong> is likely to go, measure the height of a <strong>rectangle</strong> and project it from the broken 
wall in the direction of the breakout. This is the minumum target. The maximun target is obtained by taking the lenght of the <strong>rectangle</strong> 
and project it vertically from the broken wall in the direction of a breakout.

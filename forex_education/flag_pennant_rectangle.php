A <strong>Pennant</strong> is a small <a href="triangles.php">triangle</a> whose lines are slanted in the same direction. Pennants that slant the 
<a href="trend_range.php">trend</a> serve as <a href="triangles.php">continuation patterns</a>. There is an old saying "The <strong>pennant</strong> flies at half-mast" - a rally is likely to travel as far the 
pennant as it did before. A pennant that slants in the direction of the <strong>trend</strong> indicates exhaustion - a trend is nearing a <a href="reversal_patterns.php">reversal</a>.
<center><img src="../images/content_education/pennant.gif" alt="Pennant" /></center></div><br />
<h2><a name="flag">Flag</a></h2>
<div class="textarea">A <strong>Flag</strong> is a rectangle whose boundaries are parallel but slant up or down. <strong>Breakouts</strong> tend to go against the slope of 
the flag. If a flag slants upward, a downside breakout is more likely. If the flag slants down, an upside breakout is more likely.</div>
<img class="imgcont" src="../images/content_education/flag.gif" alt="Flag" /><br />
<div class="textarea">If you see a <strong>downsloping flag</strong> in an uptrend, place a buy order above the latest peak of the flag to catch an upside breakout. 
A <strong>rising flag</strong> in an uptrend marks distribution ,and a downstide breakout is more likely. Place an order to sell short below the latest  
low of that flag. Reverse the procedure in downtrends.</div>
<br clear="all" />
<h2><a name="rectangle">Rectangle</a></h2>
<div class="textarea">A <strong>rectangle</strong> is a chart pattern that contains price movement between two parallel lines.You need four points to draw 
a rectangle: The upper lines connects two rally tops, and the lower line connects two bottoms. These lines should be drawn through the 
edges of congestion areas rather than across the extreme hight and lows. The upper line of a <strong>rectangle</strong> identifies <a href="support_resistance.php">resistance</a>, while the 
lower line identifies <a href="support_resistance.php">support</a>. The upper lines shows where bull run out of steam; the lower line shows where bears become exhausted. A 
rectangle shows that bulls and bears are evenly matched.</div> 

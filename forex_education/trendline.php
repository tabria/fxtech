  A steep trendline shows that the dominant crowd is moving rapidly.A relatively flat trendline shows that the dominant 
 crowd is moving slowly. A shallow trendline is likely to last longer.Comparing angles of trendlines shows whether the dominant 
 market crowd is becoming more bullish or bearish.
 
 <br/>&nbsp;&nbsp;&nbsp;&nbsp;Sometimes prices accelerate from their trendline. Then you can draw a new, steeper trendline. It shows 
 that a trend is speeding up, becoming unsustainable.</div>
 
  <br/><br/><div class="top2">
  <br/><center><img src="../images/content_education/trendline2.gif"alt="Steeper Trendline" /></center><br/></div>

<div class="textarea">When you draw a new, <strong>steeper trendline</strong>, tighten your stop, place it immediately below the latest trendline, and adjust that stop
 at every new bar. The breaking of a steep trendline is usually followed by a sharp reversal.
 <br/>&nbsp;&nbsp;&nbsp;&nbsp;<i>If volume expands when prices move in the direction of a trendline, it confirms that trendline; 
  if volume shrinks when prices pull back to a trendline, it also confirms the trendline. If volume expand when prices return to a 
trendline, it warns of a potential break; if volume shrink when  prices pull away from trendline, it warns that the trendline is in danger.</i>
<br/>&nbsp;&nbsp;&nbsp;&nbsp;The <a href="support_resistance.php#breakout">breaking</a> of a well-established trendline shows that the dominant market crowd has lost its power. You have to be 
careful not to anticipate trading signals - most traders lose money when they jump the gun.
 <br/>&nbsp;&nbsp;&nbsp;&nbsp;After a very steep uptrend is broken, prices often rally again, retest their old high, and touch their old uptrendline 
from below.</div>
<div class="imgspace"><img class="imgcont1" src="../images/content_education/trendline3.png" alt="Test from below" title="Trendline test from below"/></div><br/>
<div class="textarea">When that happens, you have a near-perfect shorting opportunity: a combination of a <a href="reversal_patterns.php#doubletop">double top</a>, a pullback to an old <strong>trendline</strong>, 
and perhaps a bearish divergence from technical indicators. The reverse also applies to downtrends.

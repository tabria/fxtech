               <h2>Trendline</h2>
<div class="textarea">
 <!-- square ad in content start  -->

<!-- ads 300x250 -->
<!-- square ad in content end  -->
Charts reveal the actions of bulls and bears. Bottoms of declines show where bears stopped and 
bulls regained control of the market. Peaks of rallies show where bulls ran out of steam and bears gained control. A line connecting two
 nearby bottoms shows the lowest common denominator of bullish power. A line that connects two nearby tops shows the lowest common 
 denominators of he power of bears. Those lines are called <b>trendlines</b>.</div>
<div class="textarea">
 <strong>Traders</strong> use them to identify <a href="trend_range.php">trends</a>. The most important 
 feature of a trandline is its angle - it identifies the dominant market force. When <strong>trendline</strong> points up, it shows that bulls are in 
 control. Then it pays to buy with a protective stop below the trendline.When a trendline points down, it shows that bears are in control.
  Then it pays to sell short and protect your position with stop above trendline.</div>
<div class="imgspace"><img class="imgcont1" src="../images/content_education/trendline1.png"alt="Trendline" title="Trendline"/></div>
<div class="textarea">It is better to draw the trendline through the edges of cognestion areas. Those edges show where the majority of traders have 
 reversed direction. Technical analysis is poll-taking and polltakers want to track opinions of masses, not of a few extremists.Panic 
 dumping by bulls at the bottoms and panic covering by bears at the tops create extremes, which appear as long "tails" on the charts. 
 Markets constantly fluctuate, seeking an area that generates the highest volume of trading. A tail shows that certain price has been 
 rejected by the market. It usually leads to a swing in the opposite direction. As soon sa you recognize a tail, trade against it. 
 Place your protective stop halfway through the tail. If the market starts "chewing its tail," it is time to get out.</div>
<div class="textarea">You can rate the importance of any <strong>trendline</strong> by examing five factors: the timeframe of the trendline, its length, the number of 
 times prices touch it, its angle, and volume.
   <br/>&nbsp;&nbsp;&nbsp;&nbsp;<i> - The longer timeframe, shows that the trendline is more important.</i> A trendline on a week chart identifies a more 
important trend than a daily trendline.
   <br/>&nbsp;&nbsp;&nbsp;&nbsp;<i> - The longer trendline is more valid.</i> A short trendline reflects mass  behavior over a short period.
    A longer trendline reflects mass behavior over a long time. The longer a trend continues, the gater its inertia.
  <br/>&nbsp;&nbsp;&nbsp;&nbsp;<i> - The more contacts between prices and trendline, the more valid that line</i>. When the trend is up, a 
  return to the trendline shows a lebellion among bears. When the trend is down, a rally to the trendline shows a rebellione by the bulls. 
  When price pull back to a trendline and then bounce away from it, you know that the dominant market group has beaten the rebels.
 <br/>&nbsp;&nbsp;&nbsp;&nbsp;<i> - The angle between a trendline and the horizontal axis reflects the emotional intensity of the dominant
 market crowd.</i>
 

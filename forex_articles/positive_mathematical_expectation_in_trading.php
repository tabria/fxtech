The definition of a <strong>positive mathematical expectation</strong> (PME) can be reduced to the statement that there 
exists a mathematically proven probability that the trader will end up with profits, not losses.</div>
  <div class="textarea">
Technically speaking a system that returns any positive number is a system that we would expect to make 
money with. Clearly, and all things being equal, a trader would want to trade a system with as high of a 
<a href="positive_mathematical_expectation.php">mathematical expectation</a> number as possible.</div>
<h3>Positive Mathematical Expectation - Trading System evaluation Method</h3>
  <div class="textarea">
	<p>Average Profit from deal = Total Profit / Number of winning deals;</p>

	<p>Average Loss from deal = Total Loss / Number of losing deals;</p> 
	<p>Average Risk Reward(RR) = Average Loss from deal / Average Profit from deal</p>
	<p>Results = Number of winning deals / Total number of deals;</p> 
	<p>PME = (1 + Average Profit / Average Loss) * (Number of winning deals / Total deals) - 1;</p>
	<br/>

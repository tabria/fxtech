  <div class="nohtitle">Mathematical Expectation &#150; The Definition and beyond</div> 
   <div class="textarea">It is difficult to explain this phrase with few words, but simply put, this is a mean value of future trades or in other words it is the effectiveness of the trades. This may seem a little foggy explanation, so now I will start with detailed explanations.
<br/><br/>The basic formula to use is the following:
</div>
 <div class="textarea">Mathematical Expectation = Probable Profit * Win Rate &#150; Probable Loss * Loss Rate - Spread - Swap * Days
<br/><br/>Probable Profit = Open Price &#150; Probable Profit Target Price
<br/>Probable Loss = Open Price &#150; Probable Stop Loss Price
<br/><br/>Win Rate = Number of Winning Trades / Total Trades
<br/>Loss Rate = 100% - Win Rate
<br/><br/>Spread and Swap &#150; you can get them from the broker website
<br/><br/>To use this formula you must have a predetermined stop loss and profit target. Some traders open trades without having set the stop loss level, which is not advisable, because their stoploss will be their whole account. In this case, to know what will be the &#147;Probable Stop Loss Price&#148;, you will need to calculate at what price the broker will automatically close the deal.
<br/><br/>Consider the following example:
<br/><br/>Probable Profit = $200;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Win Rate = 50%;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Spread = $1;
<br/>Probable Loss = $200;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Loss Rate = 50%;&nbsp;&nbsp;&nbsp;&nbsp;Swap = 0;
<br/><br/>Then:
<br/><br/>ME = 200*(50/100) - 200*(50/100) - 1 - 0*0 = 100 - 100 -1 - 0 = - $1
<br/><br/>It is easy to see that even in neutral conditions, our <strong>mathematical expectation (expected value)</strong> is <strong>negative</strong>. So for a sufficiently long period of time, if it is traded under these conditions, we will not win anything and will most likely go bankrupt.
<br/><br/>This example shows quite well why trading on the <a href="../forex_education/what_is_forex.php">Forex Market</a> is a high risk business. The system itself is constructed so, that the trader will have a negative mathematical expectation - pay more than it receives, which automatically puts him in a losing position, even before he has opened a single transaction. This is the major problem. Each forex trader must come to grips with it to begin to earn money from the market.
</div>
<h2>Asymmetrical Trades </h2>
 <div class="textarea">
Almost all trades that you will open will be asymmetric and the mathematical expectation is very useful to them. There are three types of asymmetry &#150; when the asymmetry is in the Win, Loss Rate, when is in the Probable Profit, Probable Loss and when is a combination of both.
<br/><br/>For example, double asymmetry might look like this: 
<br/><br/>Probable Profit = $200;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Win Rate = 40%;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Spread = $1;
<br/>Probable Loss = $250;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Loss Rate = 60%;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Swap = 0
<br/><br/>So the Mathematical expectation will be: 
<br/><br/>ME = 200*(40/100) - 250*(60/100) - 1- 0*0 = 80 - 150 - 1 - 0 = - $71
<br/><br/>Double asymmetry creates additional obstacles, since the forex trader must balance two variables simultaneously. Here the <a href="positive_mathematical_expectation_in_trading.php">mathematical expectation</a> can protect us from concluding a pre-doomed trades. This can be done by determining whether the trade is highly asymmetric, and consequently to decide whether it is better to open the trade in the opposite direction of the original.
<br/><br/>To be a profitable forex trader, you must come to grips with the <strong>negative mathematical expectation</strong>. Do not be fooled, this is quite a difficult task for execution. Nothing is impossible. With more training and testing everything is achieved. 
